<div class="wrap">
    <h1> Members Shortcode </h1>

    <p> If you are interested in showing the members of the iuma, use the 
    following shortcode: </p>
    
    <p><code>[iuma-members]</code></p>

    <p> However, in case that you are interested in order to visualize the members 
    of an <b>specfic IUMA division</b>, you can use the following shortcode:</p>
    
    <p><code>[iuma-members division='[tag]']</code></p>

    <p> Where [tag] is a the parameter to indicate the name of the division. Currently,
    the possible divisions are as follows:  </p>

    <ul style="list-style: square; margin-left: 25px;">
        <li style="margin-bottom: 2px;">COM</li>
        <li style="margin-bottom: 2px;">DSI</li>
        <li style="margin-bottom: 2px;">MAGIC</li>
        <li style="margin-bottom: 2px;">MEMS</li>
        <li style="margin-bottom: 2px;">SICAD</li>
        <li style="margin-bottom: 2px;">TI</li>
        <li style="margin-bottom: 2px;">TME</li>
    </ul>  
</div>